lst1 = [0, 8, 7, 6, 3, 2]
# lst1 =[5]
result = 0                        #Змінна з нульовим значенням
if len(lst1) > 0:                 #Якщо сума lst > 0, виконується цикл
    for i in range(len(lst1)):
        if i % 2 == 0:            #Відбираємо числа з парними індексами
            result += lst1[i]     #Додаємо числа з парними індексами
    result *= lst1[-1]            #Множимо на останнє число в списку
print(result)











